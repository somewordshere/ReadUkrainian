import fs from "node:fs/promises";
import path from "node:path";
import vm from "node:vm";

const rootDir = process.cwd();
const files = [
  "js/data/stories.js",
  "js/data/a1-stories.js",
  "js/data/a2-stories.js",
  "js/data/b1-stories.js",
];

const context = {};
vm.createContext(context);

const combinedSourceParts = [];

for (const relativeFile of files) {
  const absoluteFile = path.join(rootDir, relativeFile);
  const source = await fs.readFile(absoluteFile, "utf8");
  combinedSourceParts.push(`\n// ${relativeFile}\n${source}`);
}

combinedSourceParts.push("\nglobalThis.__storiesByLevel = storiesByLevel;\n");

vm.runInContext(combinedSourceParts.join("\n"), context, { filename: "stories-bundle.js" });

const levels = Object.keys(context.__storiesByLevel || {}).sort();
const records = [];

for (const level of levels) {
  const stories = context.__storiesByLevel[level] || [];
  for (const [index, story] of stories.entries()) {
    records.push({
      level,
      sortOrder: index + 1,
      title: story.title,
      paragraphs: story.paragraphs || [],
      showWordCount: story.showWordCount !== false,
      active: story.active !== false,
    });
  }
}

const seedDir = path.join(rootDir, "data");
await fs.mkdir(seedDir, { recursive: true });
await fs.writeFile(
  path.join(seedDir, "content-seed.json"),
  `${JSON.stringify(records, null, 2)}\n`,
  "utf8"
);

const sqlLines = [
  "-- Generated by scripts/build-content-seed.mjs",
  "DELETE FROM texts;",
];

for (const record of records) {
  const safeLevel = record.level.replace(/'/g, "''");
  const safeTitle = record.title.replace(/'/g, "''");
  const safeParagraphs = JSON.stringify(record.paragraphs).replace(/'/g, "''");

  sqlLines.push(
    `INSERT INTO texts (level, display_order, title, paragraphs_json, show_word_count, is_enabled) VALUES ('${safeLevel}', ${record.sortOrder}, '${safeTitle}', '${safeParagraphs}', ${record.showWordCount ? 1 : 0}, ${record.active ? 1 : 0});`
  );
}

await fs.writeFile(
  path.join(rootDir, "migrations", "0002_seed_texts.sql"),
  `${sqlLines.join("\n")}\n`,
  "utf8"
);

console.log(`Generated ${records.length} seed records.`);
